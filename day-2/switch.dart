void main() {
  String dayOfWeek = "Fri";

  switch (dayOfWeek) {
    case "Mon":
      print("Today is Monday");
      break;
    case "Tue":
      print("Today is Tuesday");
      break;
    case "Wed":
      print("Today is Wednesday");
      break;
    case "Thu":
      print("Today is Thursday");
      break;
    case "Fri":
      print("Today is Friday");
      break;
    case "Sat":
      print("Today is Saturday");
      break;
    case "Sun":
      print("Today is Sunday");
      break;
    default:
      print("Invalid day");
  }
}
